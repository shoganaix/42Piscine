#include <unistd.h>

void	ft_swap(int *a, int *b)
{
	int	aux;

	aux = *a;
	*a = *b;
	*b = aux;
}

int	main(void)
{
	int	a;
	int	b;

	a= 5;
	b = 3;
	ft_swap(&a, &b );
	printf("%d", a);
	return (0);
}
